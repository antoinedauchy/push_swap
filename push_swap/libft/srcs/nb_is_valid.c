#include "../libft.h"

int			nb_is_valid(char *s, int *nb)
{
	int		n;
	int		minus;
	long	l_nb;

	n = 0;
	minus = 0;
	l_nb = 0;
	if (s[n] == '-' || s[n] == '+')
	{
		minus = 1;
		n += 1;
	}
	while (s[n] >= '0' && s[n] <= '9' && n < 11)
	{
		l_nb = l_nb * 10 + s[n] - '0';
		n += 1;
	}
	if (minus)
		l_nb = -l_nb;
	if (s[n] || l_nb > 2147483647 || l_nb < -2147483648)
		return (0);
	*nb = (int)l_nb;
	return (1);
}
